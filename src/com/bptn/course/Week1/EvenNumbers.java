public class EvenNumbers {

		public static void main(String[] args) {

		    int evenNumber = 100;

		    System.out.println("Printing the even numbers from 1 to 100: ");

		    for (int i = 2; i <= evenNumber; i++)

		     if( i % 2 == 0 )
		        {
		            System.out.print( i + " " );
		        }
		}

		}